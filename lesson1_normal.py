# Задача-3: Напишите программу, вычисляющую корни квадратного уравнения вида
import math
print("Введите значения коэффициентов квадратного уравнения:\n")
a = float(input("a = "))
b = float(input("b = "))
c = float(input("c = "))
_D_ = b*b - 4*a*c
if _D_ >= 0:
    x1 = (- b + math.sqrt(_D_))/(2*a)
    x2 = (- b - math.sqrt(_D_))/(2*a)
    print("Корни квадратного уравнения x1 = ", x1, ", x2 = ", x2)
else:
    print("Уравнение не имеет действительных корней")
