# Задача-1: Дано произвольное целое число, вывести самую большую цифру этого числа.
number = input("Введите произвольное целое число\n")
n = 0
max = 0
while n < len(number):
    if max < int(number[n]):
        max = int(number[n])
        n += 1
    else:
        n += 1
print(max)

